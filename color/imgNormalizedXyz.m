function J = imgNormalizedXyz(w, h)

	[X, Y] = meshgrid(0:1/(w-1):1, 0:1/(h-1):1);
	I = zeros(h, w, 3);
	I(:,:,1) = X;
	I(:,:,2) = 1-Y;
	I(:,:,3) = 1 - I(:, :, 1) - I(:, :, 2);

	J(:,:,1) =  2.3646 * I(:, :, 1) + -0.8965 * I(:, :, 2) + -0.4681 * I(:, :, 3);
	J(:,:,2) = -0.5152 * I(:, :, 1) +  1.4264 * I(:, :, 2) + -0.0888 * I(:, :, 3);
	J(:,:,3) =  0.0052 * I(:, :, 1) + -0.0144 * I(:, :, 2) +  1.0092 * I(:, :, 3);
	
	M1 = max(J, [], 3) > 1;
	M1 = [M1, M1, M1];
	
	M2 = min(J, [], 3) < 0;
	M2 = [M2, M2, M2];
	
	M3 = M1 .* M2 > 0;
	
	J(M1) = 1;
	J(M2) = 0;
	J(M3) = 0.5;

	J(h - round(h/3), round(w/3), :) = 1;
	imshow(J);

end